#include <stdio.h>

/**
 * add- addes two numbers
 * @a: value 1
 * @b: value 2
 * Return: the sum of a and b
 */
int add(int a, int b)
{
		return (a + b);
}


/**
 * sub - addes two numbers
 * @a: value 1
 * @b: value 2
 * Return: the sum of a and b
 */
int sub(int a, int b)
{
		return (a - b);
}

/**
 * div - addes two numbers
 * @a: value 1
 * @b: value 2
 * Return: the sum of a and b
 */
int div(int a, int b)
{
		return (a / b);
}

/**
 * mul - addes two numbers
 * @a: value 1
 * @b: value 2
 * Return: the sum of a and b
 */
int mul(int a, int b)
{

		return (a * b);
}

/**
 * mod - addes two numbers
 * @a: value 1
 * @b: value 2
 * Return: the sum of a and b
 */
int mod(int a, int b)
{

		return (a % b);
}
